//// Sorting algo

//// Elementary Sorting Algos :
// less commonly used because they are less efficient, quadratic
// Bubble sort -> Time best O(n), average O(n^2), worst O(n^2) / Space O(1)
// Selection sort -> Time best O(n), average O(n^2), worst O(n^2) / Space O(1)
// Insertion sort -> Time best O(n^2), average O(n^2), worst O(n^2) / Space O(1)

// Build-in JS .sort()
// .sort((a,b)=> a-b) -> ascending
// .sort((a,b)=> b-a) -> descending

//// More Complex Sorting Algos (Intermediate) -> improve time complexity from O(n^2) to O(nlogn)
// Merge sort -> Time O(nlogn)/ Space O(n)
// Quick sort -> Time best/average O(nlogn), worst O(n^2) / Space O(n)
// Radix sort -> Time O(nlogn) / Space O(n)

//// Big-O Complexity
//// O(n!) > O(2^n) > O(n^2) > O(n logn) > O(n) > O(logn), O(1)

